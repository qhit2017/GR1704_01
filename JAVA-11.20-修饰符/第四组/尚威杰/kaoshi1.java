package 考试题;

import java.util.Scanner;
//从键盘输入一个数 如果是正数就输出这个数是正数 如果是负数 就输出这个数是负数
public class 考试1 {

	public static void main(String[] args) {
		
		Scanner sc =new Scanner(System.in);
		System.out.println("请输入一个数：");
		int i =sc.nextInt();
		if (i>0){
			System.out.println("正数");
		}else{
			System.out.println("负数");
	
		
		
	}
	
}
}