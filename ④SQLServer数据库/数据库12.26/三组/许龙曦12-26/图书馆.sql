--5.	查询哪本书被借的次数最多，输出书名和次数
SELECT MAX(b.bname),COUNT(*)
from BOOKS b INNER JOIN BORROW bw
on b.bno=bw.bno
--6.查询陈启航分别借了哪些书
SELECT b.bname
from BOOKS b INNER JOIN BORROW bw
on b.bno=bw.bno
INNER JOIN CARD c
ON c.Cno=bw.cno
AND c.Name='貂蝉'
--7.查询作者姓张的书，都被哪些读者借阅，输出书名、作者、读者
SELECT b.bname,b.author,c.Cno
from BOOKS b INNER JOIN BORROW bw
on b.bno=bw.bno
INNER JOIN CARD c
ON c.Cno=bw.cno
AND b.author='韩信'
--8.	查询现有图书中价格最高的图书，输出书名及作者。
SELECT top 1 b.bname,b.author
from BOOKS b
GROUP BY b.Price,b.bname,b.author
ORDER BY b.Price desc
