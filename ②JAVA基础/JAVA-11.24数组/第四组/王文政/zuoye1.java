package com.王文政;

public class zuoye1 {
	
	public static void main(String[] args) {
		
		//正着输出
		
		   int []arr={8,51,63,21,47,12};
		
		for(int rs=0;rs<arr.length;rs++){
			System.out.println(arr[rs]);
		}
		   //反向输出
			for(int rs=arr.length-1;rs>=0;rs--){
				System.out.println(arr[rs]);
			}
		
		
		
	}
	

}
